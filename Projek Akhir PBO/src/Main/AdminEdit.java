    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:

        try{
            Connection c = SQLConnection.getKoneksi();
            Statement s = c.createStatement();
            String id = no.getText();
            String NamaKa = namaka.getText();
            String Berangkat = berangkat.getText();
            String Tiba = tiba.getText();
            String Kursi = kursi.getText();
            
            try {
                String sql = "UPDATE jadwal SET namaka = '"+NamaKa+"', berangkat = '"+Berangkat+"', tiba = '"+Tiba+"', jumlahkursi = '"+Kursi+"' WHERE nomorka = '"+id+"' ";
                PreparedStatement p = c.prepareStatement(sql);
                p.executeUpdate(sql);
                p.close();
                JOptionPane.showMessageDialog(null, "Sukses mengedit data kereta api!");
            } catch(SQLException e){
                System.out.println("Error");
            }
        }catch(Exception e){
            System.out.println("Check error");
        }
    }//GEN-LAST:event_submitButtonActionPerformed

