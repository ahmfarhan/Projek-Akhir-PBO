    public void SetRecord(){
        try{
            Connection c = SQLConnection.getKoneksi();
            Statement s = c.createStatement();
            String id = no.getText().toString().trim();
            String sql = "SELECT * FROM jadwal WHERE Nomorka = '"+id+"' ";
            ResultSet r = s.executeQuery(sql);
            if (r.next()) {
                namaka.setText(r.getString("Namaka"));
                berangkat.setText(r.getString("Berangkat"));
                tiba.setText(r.getString("Tiba"));
                kursi.setText(r.getString("Jumlahkursi"));
            }else{
                System.out.println("Error!");
            }
            s.close();
            r.close();
        }catch(Exception e){
            System.out.println("Check error");
        }
    }

